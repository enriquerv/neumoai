			<h2 class="mt-5">Pronóstico de la Neumonía</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">
					El pronóstico de la neumonía adquirida en la comunidad (NAC) se determina por tres factores principales: edad del paciente, estado general de salud (presencia de comorbilidades) y la severidad o gravedad en cómo se presenta la neumonía.
				</p>
				<p class="mt-4 text-justify">
					En general, la tasa de mortalidad en pacientes tratados ambulatorios es inferior al 1%, mientras que en los pacientes hospitalizados la tasa de mortalidad oscila entre el 5% y el 15%, pero aumenta entre el 20% y 50% en los pacientes que requieren ingreso en la Unidad de Cuidados Intensivos (UCI).
				</p>
				
				<p class="mt-4 text-justify">
					Existen varios factores de riesgo que se asocian con la mortalidad en la neumonía: la bacteriemia o presencia de microorganismos en la sangre, la admisión en la UCI, las enfermedades crónicas (especialmente la enfermedad neurológica) y la neumonía causada por un patógeno resistente a los antibióticos (Staphylococcus aureus, Pseudomonas aeruginosa, Enterobacteriaceae).
				</p>
				
				<p class="mt-4 text-justify">
					El reingreso hospitalario en los pacientes con NAC oscila entre el 7% y el 12%. En la mayoría de los casos, la razón principal del reingreso hospitalario es debido a la exacerbación relacionada con las enfermedades crónicas, principalmente, cardiovasculares, pulmonares o neurológicas.
				</p>
			</div>

			
			<h2 class="mt-5">Evolución clínica de la Neumonía</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">
					El cumplimiento del tratamiento antibiótico es muy importante  para el paciente con neumonía, incluso después de que se sienta una mejoría clínica. Si después de 48 – 72 horas de recibir tratamiento antibiótico no se observa una mejora  o los síntomas empeoran, se debe contactar con el médico.
				</p>
				<p class="mt-4 text-justify">
					Los pacientes que han tenido una neumonía deben ser visitados 4-6 semanas después de su recuperación. En esta visita de control se puede realizar una nueva radiografía de tórax que sirve para confirmar que la neumonía se ha resuelto sin complicaciones.
				</p>
				
				<p class="mt-4 text-justify">
					Si el paciente es fumador, se aconseja abandonar el hábito tabáquico.
				</p>
			</div>


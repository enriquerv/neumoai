						<span class="strong">Radiografia simple de tórax.</span> En la neumonía, los alvéolos, que deberían estar llenos de aire, se llenan de líquido o tejido inflamatorio, por lo que en una radiografía esto se observa de color blanco, mientras que el espacio lleno de aire aparece de color oscuro. La presencia de este color blanco confirma el diagnóstico de la infección.

			<h2 class="mt-5">Detección de antígenos en orina</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">Se pueden emplear diversas técnicas microbiológicas para detectar la presencia de ciertos patógenos en distintas muestras biológicas, fundamentalmente esputo, suero, orina. La detección de antígenos en orina es una técnica de diagnóstico rápido que permite la detección de antígenos de Streptococcus pneumoniae y Legionella pneumophila serogrupo 1. </p>
				<p class="mt-4 text-justify">Los tests de antígenos en orina para S. pneumoniae y Legionella ofrecen resultados en solo 15 minutos, lo que permite iniciar el tratamiento con el antibiótico dentro de las 4 horas posteriores a la hospitalización. Esta prueba tiene una gran especificidad 97-100%, pero su sensibilidad oscila entre un 57% y un 87%, aunque es algo mayor en enfermedad invasiva. La determinación del antígeno en orinas concentradas aumenta su sensibilidad.</p>

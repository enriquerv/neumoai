			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">La neumonía es la infección del pulmón que ocasiona inflamación y daño del tejido pulmonar. Los pulmones están formados por conducciones por los que circula el aire (bronquios) y pequeños sacos donde se realiza el intercambio de gases (alvéolos).</p>
				<p class="mt-4 text-justify">En una persona sana los alvéolos se llenan de aire al respirar, mientras que en una persona con neumonía los alvéolos están llenos de pus y líquido, por lo que el intercambio de gases se ve alterado.</p>
			</div>

			<h2 class="mt-5">Los pulmones</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">Los pulmones están localizados en la cavidad torácica y son los encargados de mantener la oxigenación de la sangre mediante la respiración. Al respirar, el aire entra en el cuerpo, por la nariz o por la boca, baja por la garganta, a través de la laringe y de la tráquea, llega a los pulmones, a través de los bronquios principales y, finalmente, a los alvéolos. Los alvéolos son pequeños sacos de paredes finas en estrecho contacto con los vasos sanguíneos y es donde se realiza el intercambio de oxígeno, que pasa a la sangre, y el dióxido de carbono, que se expulsa al exterior. El corazón bombea sangre del cuerpo a los pulmones, y en los alvéolos esta se oxigena. Luego, el corazón bombea la sangre recién oxigenada al resto del cuerpo. Una persona sana respira ente 10 y 15 veces por minuto.</p>
			</div>

			<h2 class="mt-5">Tipos de neumonía</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">De acuerdo a la presentación clínica de la neumonía, se pueden diferenciar en dos grupos:</p>
				<ul>
					<li class="mt-4 text-justify">
						<span class="strong">Neumonía adquirida en la comunidad (NAC).</span> 
						Es la neumonía típica que se caracteriza por una presentación brusca, fiebre elevada, dolor en puntada de costado o dolor abdominal transitorio, respiración rápida (taquipnea), tos y expectoración purulenta o herrumbrosa. Un ejemplo de este tipo de presentación típica es la neumonía causada por el Streptococcus pneumoniae (neumococo) considerado el principal microorganismo causante de la neumonía a nivel mundial.</li>
					<li class="mt-4 text-justify">
						<span class="strong">Neumonía adquirida en la comunidad con presentación clínica atípica.</span>
						Es la neumonía producida por virus o bacterias atípicas y se caracteriza por un  inicio más leve, fiebre baja, tos escasamente productiva, diarrea y/o vómitos. La neumonía causada por bacterias intracelulares, como por ejemplo Mycoplasma pneumoniae o Legionella pneumophila, son un ejemplo de una neumonía de presentación atípica.</li>
				</ul>
			</div>
			
			<h2>¿A cuántas personas afecta la Neumonía?</h2>
			<p class="text-center mt-3 w100">
				<img src="{{ env('APP_URL') }}/assets/images/neumonia/intro02.png" class="img-intro" data-aos="zoom-in" data-aos-duration="1000">	

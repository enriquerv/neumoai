			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">La neumonía es una infección  muy frecuente. Según datos de la Organización Mundial de la Salud (OMS), en el año 2015 la neumonía y la gripe (influenza) juntas fueron la tercera causa de mortalidad global.</p>
				<p class="mt-4 text-justify">En Europa la incidencia de la neumonía adquirida en la comunidad (NAC) se estima en 2-10 casos por cada 1.000 habitantes/año. Los niños menores de 5 años y los adultos mayores de 65 años son los más vulnerables.</p>
				<p class="mt-4 text-justify">En España se calcula que entre el 20% y 70% de los casos necesitan ingreso hospitalario y, aproximadamente, un 9% de los pacientes diagnosticados de neumonía necesitan ingresar en una unidad de cuidados intensivos (UCI) para su monitorización.</p>
				<p class="mt-4 text-justify">Se estima que la mortalidad de la NAC oscila entre el 5 -7%. Sin embargo, esta mortalidad puede llegar a un 25% en los pacientes ingresados en la UCI.</p>

			<h2>¿Qué es la Neumonía?</h2>
			<p class="text-center mt-3 w100">
				<img src="{{ env('APP_URL') }}/assets/images/neumonia/intro01.png" class="img-intro" data-aos="zoom-in" data-aos-duration="1000">	

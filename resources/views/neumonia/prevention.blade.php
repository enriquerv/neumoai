			<h2 class="mt-5">Prevención de la Neumonía</h2>
			<div class="col-md-10 offset-1">
				<p class="mt-4 text-justify">En la prevención de la Neumonía Adquirida en la Comunidad (NAC) son fundamentales las siguientes medidas:</p>
			</div>
			<div class="col-md-10 offset-1 row">
				<div class="col-md-4 text-center">
					<img src="{{ env('APP_URL') }}/assets/images/neumonia/causas01.svg" alt="">
				</div>
				<div class="col-md-8">
					<p class="mt-4 text-justify">
						<span class="strong">La vacuna antineumocócica.</span> El neumococo (Streptococcus pneumoniae) es una bacteria que tiene, aproximadamente, 98 tipos diferentes. La vacuna PCV13 protege contra 13 tipos diferentes de la bacteria neumococo y la PPSV23 protege contra 23 tipos diferentes.
						<br><br>
						La vacuna PCV13 se recomienda para las siguientes personas:
						<br><br>
						<ul>
							<li>Todos los adultos de 65 años o mayores.</li>
							<li>Los adultos de 19 años o mayores que tengan ciertas afecciones.</li>
						</ul>
						<br><br>
						La vacuna PPSV23 se recomienda para las siguientes personas:
						<br><br>
						<ul>
							<li>Todos los adultos de 65 años o mayores.</li>
							<li>Los adultos de los 19 a los 64 años de edad que tengan ciertas afecciones o que sean fumadores activos.</li>
						</ul>
						<br><br>
						Hay que tener en cuenta que las vacunas PCV13 y PPSV23 no se deben administrar al mismo tiempo. Si se recomiendan las dos vacunas, se debe recibir primero una dosis de la PCV13, seguida de una dosis de la PPSV23 en otra visita médica, en general al cabo de 6 meses.
					</p>
				</div>
			</div>
			<div class="col-md-10 offset-1 row">
				<div class="col-md-4 text-center">
					<img src="{{ env('APP_URL') }}/assets/images/neumonia/causas02.svg" alt="">
				</div>
				<div class="col-md-8">
					<p class="mt-4 text-justify">
						<span class="strong">La vacuna antigripal o vacuna contra la gripe (influenza).</span> La gripe o influenza es una enfermedad que en algunos casos puede llevar a la hospitalización y, en ciertas ocasiones, incluso provocar la muerte. En España, la epidemia gripal ocurre desde el final del otoño hasta el principio de la primavera. En general, la gripe afecta al 1–5% de la población, sin embargo, en casos de epidemia, puede llegar afectar al 40–50% de las personas mayores de 65 años.
						<br><br>
						Aproximadamente, dos semanas después de la administración de la vacuna antigripal ya se detectan anticuerpos contra el virus de la gripe. Estos anticuerpos brindan protección contra la infección con los virus incluidos en la vacuna. La vacuna antigripal puede llegar a prevenir la enfermedad en un 70–90% de las personas sanas mayores de 65 años.
						<br><br>
						La vacuna antigripal se debe administrar a todas las personas con un mayor riesgo de complicaciones (ancianos, personas con enfermedades crónicas, embarazadas) o en personas sanas que estén en estrecho contacto con personas de alto riesgo de desarrollarlas (personal sanitario).
						<br><br>
						Se recomienda a las personas alérgicas al huevo, hablar con su médico de cabecera antes de recibir la vacuna.
					</p>
				</div>
			</div>

				<div class="col-md-8">
					<p class="mt-4 text-justify">
						<span class="strong">Asegurar un estado nutricional adecuado.</span> La malnutrición no solo incrementa el riesgo de sufrir una neumonía, sino que incrementa el riesgo de muerte en el curso de la neumonía. Asegurar un adecuado estado nutricional es muy importante para prevenir infecciones como la neumonía.<br><br>
				</div>
			</div>


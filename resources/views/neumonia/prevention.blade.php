					<p class="mt-4 text-justify">
						<span class="strong">Evitar el contacto con niños enfermos.</span> Evitar el contacto con niños que estén enfermos, sobre todo con aquellos que tengan una infección respiratoria. Esta recomendación, sobre todo, va dirigida a las personas adultas mayores no vacunadas y que sufran de alguna enfermedad crónica que les haga más vulnerables a las infecciones.<br><br>
					</p>
				</div>
			</div>


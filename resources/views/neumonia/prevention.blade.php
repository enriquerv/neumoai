				<div class="col-md-8">
					<p class="mt-4 text-justify">
						<span class="strong">Reducir el consumo de alcohol.</span> El consumo excesivo de alcohol está asociado con el mayor riesgo de que la neumonía sea más grave, debido a que afecta a componentes esenciales del sistema inmune. A esto se le añade que las personas alcohólicas, con frecuencia, desarrollan daño hepático, así como malnutrición, lo que  incrementa el riesgo de tener neumonía.<br><br>
					</p>
				</div>
			</div>

			<div class="col-md-10 offset-1 row">

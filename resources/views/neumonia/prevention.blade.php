				<div class="col-md-8">
					<p class="mt-4 text-justify">
						<span class="strong">Dejar de fumar.</span> Fumar incrementa por 4 el riesgo de tener una NAC. En los pacientes jóvenes, esto aumenta el riesgo de sufrir una NAC invasiva (bacteremia). El abandono del hábito tabáquico disminuye a la mitad el riesgo de NAC en los siguientes 5 años.<br><br>
					</p>
				</div>
			</div>

			<div class="col-md-10 offset-1 row">
				<div class="col-md-4 text-center">

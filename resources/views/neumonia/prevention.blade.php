					<p class="mt-4 text-justify">
						<span class="strong">Mantener una adecuada higiene bucal.</span> Una mala higiene bucal incrementa el riesgo de tener una neumonía. La mala higiene bucal contribuye a la formación de placa y colonización de la boca por microorganismos que pueden alcanzar los pulmones cuando ocurren microaspiraciones. La mayoría de los estudios que demuestran esta asociación entre la mala higiene bucal y el riesgo de neumonía, se han enfocado especialmente en el paciente adulto mayor y en aquellas personas que están en una residencia. Por ello, se recomienda a estas poblaciones en especial, la visita al dentista, como mínimo una vez al año.<br><br>
					</p>
